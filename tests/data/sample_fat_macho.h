#ifndef SAMPLE_FAT_MACHO_H
#define SAMPLE_FAT_MACHO_H

#include <defs.h>

// Minimal FAT (universal) Mach-O with one x86_64 slice (the sample_macho binary)
static u8_t sample_fat_macho[] = {
    // fat_header (8 bytes, big-endian)
    0xca, 0xfe, 0xba, 0xbe,  // magic    = 0xCAFEBABE (FAT_MAGIC)
    0x00, 0x00, 0x00, 0x01,  // nfat_arch = 1

    // fat_arch (20 bytes, big-endian)
    0x01, 0x00, 0x00, 0x07,  // cputype    = 0x01000007 (CPU_TYPE_X86_64)
    0x00, 0x00, 0x00, 0x03,  // cpusubtype = 3
    0x00, 0x00, 0x00, 0x1c,  // offset     = 28 (right after fat_header + fat_arch)
    0x00, 0x00, 0x00, 0x68,  // size       = 104 (size of the embedded Mach-O)
    0x00, 0x00, 0x00, 0x00,  // align      = 0

    // embedded mach_header_64 (32 bytes, little-endian)
    0xcf, 0xfa, 0xed, 0xfe,  // magic      = 0xFEEDFACF (MH_MAGIC_64)
    0x07, 0x00, 0x00, 0x01,  // cputype    = 0x01000007 (CPU_TYPE_X86_64)
    0x03, 0x00, 0x00, 0x00,  // cpusubtype = 3
    0x02, 0x00, 0x00, 0x00,  // filetype   = 2 (MH_EXECUTE)
    0x01, 0x00, 0x00, 0x00,  // ncmds      = 1
    0x48, 0x00, 0x00, 0x00,  // sizeofcmds = 0x48 = 72
    0x85, 0x00, 0x20, 0x00,  // flags      = 0x00200085
    0x00, 0x00, 0x00, 0x00,  // reserved   = 0

    // embedded segment_command_64 (72 bytes) - LC_SEGMENT_64 for __TEXT
    0x19, 0x00, 0x00, 0x00,  // cmd     = 0x19 (LC_SEGMENT_64)
    0x48, 0x00, 0x00, 0x00,  // cmdsize = 72
    // segname = "__TEXT" padded to 16 bytes
    0x5f, 0x5f, 0x54, 0x45, 0x58, 0x54, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // vmaddr = 0x0000000100000000
    0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00,
    // vmsize = 0x0000000000001000
    0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // fileoff = 0
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // filesize = 0x68 = 104
    0x68, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // maxprot  = 7 (VM_PROT_READ | VM_PROT_WRITE | VM_PROT_EXECUTE)
    0x07, 0x00, 0x00, 0x00,
    // initprot = 5 (VM_PROT_READ | VM_PROT_EXECUTE)
    0x05, 0x00, 0x00, 0x00,
    // nsects = 0
    0x00, 0x00, 0x00, 0x00,
    // flags  = 0
    0x00, 0x00, 0x00, 0x00,
};

#endif
